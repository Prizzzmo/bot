        if not content or len(content) < 50:
            return [f"üìö *{topic}*\n\n{content}"]
        
        # –†–∞–∑–±–∏–≤–∞–µ–º –∫–æ–Ω—Ç–µ–Ω—Ç –ø–æ —Å—Ç—Ä–æ–∫–∞–º –∏ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª—è–µ–º –ø–æ –≥–ª–∞–≤–∞–º
        lines = content.split('\n')
        current_chapter = None
        
        for line in lines:
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Å—Ç—Ä–æ–∫–∞ –∑–∞–≥–æ–ª–æ–≤–∫–æ–º –≥–ª–∞–≤—ã
            for i, chapter in enumerate(chapters, 1):
                # –£–ª—É—á—à–µ–Ω–Ω–æ–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –∑–∞–≥–æ–ª–æ–≤–∫–∞ –≥–ª–∞–≤—ã
                if (chapter.lower() in line.lower() or 
                    f"{i}." in line or 
                    f"{i}:" in line or 
                    (i == 1 and ("–≤–≤–µ–¥–µ–Ω–∏–µ" in line.lower() or "–∏—Å—Ç–æ–∫–∏" in line.lower())) or
                    (i == 2 and ("—Å–æ–±—ã—Ç–∏—è" in line.lower() or "—Ö—Ä–æ–Ω–æ–ª–æ–≥–∏—è" in line.lower())) or
                    (i == 3 and ("–ª–∏—á–Ω–æ—Å—Ç–∏" in line.lower() or "–¥–µ—è—Ç–µ–ª–∏" in line.lower())) or
                    (i == 4 and ("–º–∏—Ä" in line.lower() or "–º–µ–∂–¥—É–Ω–∞—Ä–æ–¥–Ω—ã–π" in line.lower())) or
                    (i == 5 and ("–∑–Ω–∞—á–µ–Ω–∏–µ" in line.lower() or "–≤–ª–∏—è–Ω–∏–µ" in line.lower() or "–∏—Ç–æ–≥–∏" in line.lower()))):
                    current_chapter = chapter
                    if current_chapter not in chapter_contents:
                        chapter_contents[current_chapter] = []
                    break
            
            # –î–æ–±–∞–≤–ª—è–µ–º —Å—Ç—Ä–æ–∫—É –∫ —Ç–µ–∫—É—â–µ–π –≥–ª–∞–≤–µ
            if current_chapter:
                # –ß–∏—Å—Ç–∏–º —Å—Ç—Ä–æ–∫—É –æ—Ç –Ω—É–º–µ—Ä–∞—Ü–∏–∏ –∑–∞–≥–æ–ª–æ–≤–∫–∞, –µ—Å–ª–∏ –µ—Å—Ç—å
                cleaned_line = line
                if current_chapter in line:
                    for i, chapter in enumerate(chapters, 1):
                        if chapter in line:
                            # –£–¥–∞–ª—è–µ–º –Ω–æ–º–µ—Ä –≥–ª–∞–≤—ã –∏ –Ω–∞–∑–≤–∞–Ω–∏–µ –∏–∑ —Å—Ç—Ä–æ–∫–∏
                            cleaned_line = re.sub(rf'\d+[\.\:\)]?\s*{re.escape(chapter)}', '', line, flags=re.IGNORECASE)
                            cleaned_line = cleaned_line.strip()
                
                if cleaned_line:  # –î–æ–±–∞–≤–ª—è–µ–º —Ç–æ–ª—å–∫–æ –Ω–µ–ø—É—Å—Ç—ã–µ —Å—Ç—Ä–æ–∫–∏
                    chapter_contents[current_chapter].append(cleaned_line)
            # –ï—Å–ª–∏ –µ—â–µ –Ω–µ –æ–ø—Ä–µ–¥–µ–ª–∏–ª–∏ –≥–ª–∞–≤—É, –∏—â–µ–º —É–ø–æ–º–∏–Ω–∞–Ω–∏—è —Ç–µ–º –≤ —Ç–µ–∫—Å—Ç–µ
            elif line.strip():
                found_chapter = False
                for chapter in chapters:
                    if any(keyword in line.lower() for keyword in chapter.lower().split()):
                        current_chapter = chapter
                        if current_chapter not in chapter_contents:
                            chapter_contents[current_chapter] = []
                        chapter_contents[current_chapter].append(line)
                        found_chapter = True
                        break
                
                # –ï—Å–ª–∏ –Ω–µ –Ω–∞—à–ª–∏ –ø–æ–¥—Ö–æ–¥—è—â—É—é –≥–ª–∞–≤—É, –¥–æ–±–∞–≤–ª—è–µ–º –≤ –ø–µ—Ä–≤—É—é
                if not found_chapter and chapters and chapters[0] not in chapter_contents:
                    current_chapter = chapters[0]
                    chapter_contents[current_chapter] = [line]
        
        # –ï—Å–ª–∏ –Ω–µ —É–¥–∞–ª–æ—Å—å —Ä–∞–∑–±–∏—Ç—å –Ω–∞ –≥–ª–∞–≤—ã, –∑–Ω–∞—á–∏—Ç —Ñ–æ—Ä–º–∞—Ç –æ—Ç–≤–µ—Ç–∞ –æ—Ç API –æ—Ç–ª–∏—á–∞–µ—Ç—Å—è
        # –ü–æ–ø—Ä–æ–±—É–µ–º —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–∏—Ç—å —Ç–µ–∫—Å—Ç –ø–æ –≥–ª–∞–≤–∞–º —ç–≤—Ä–∏—Å—Ç–∏—á–µ—Å–∫–∏
        if not chapter_contents and lines:
            # –†–∞–∑–±–∏–≤–∞–µ–º –≤–µ—Å—å —Ç–µ–∫—Å—Ç –ø—Ä–∏–º–µ—Ä–Ω–æ –Ω–∞ 5 —Ä–∞–≤–Ω—ã—Ö —á–∞—Å—Ç–µ–π
            chunk_size = max(5, len(lines) // 5)
            for i, chapter in enumerate(chapters):
                start_idx = i * chunk_size
                end_idx = (i + 1) * chunk_size if i < 4 else len(lines)
                if start_idx < len(lines):
                    chapter_contents[chapter] = lines[start_idx:end_idx]
        
        # –§–æ—Ä–º–∏—Ä—É–µ–º —Å–æ–æ–±—â–µ–Ω–∏—è –¥–ª—è –∫–∞–∂–¥–æ–π –≥–ª–∞–≤—ã
        messages = []
        
        # –°–Ω–∞—á–∞–ª–∞ –¥–æ–±–∞–≤–ª—è–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ —Å —Ç–µ–º–æ–π –≤ –ø—Ä–∏–≤–ª–µ–∫–∞—Ç–µ–ª—å–Ω–æ–º —Ñ–æ—Ä–º–∞—Ç–µ
        title_message = f"üìö *{topic.upper()}*"
        
        # –î–æ–±–∞–≤–ª—è–µ–º –¥–µ–∫–æ—Ä–∞—Ç–∏–≤–Ω—ã–π —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å
        title_message += "\n\n‚îè‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îì"
        
        # –ó–∞—Ç–µ–º –¥–æ–±–∞–≤–ª—è–µ–º –æ–≥–ª–∞–≤–ª–µ–Ω–∏–µ –≤ —É–ª—É—á—à–µ–Ω–Ω–æ–º —Ñ–æ—Ä–º–∞—Ç–µ
        toc = "\n\nüìã *–û–ì–õ–ê–í–õ–ï–ù–ò–ï:*\n"
        for i, chapter in enumerate(chapters, 1):
            emoji = chapter_emoji.get(chapter, "‚Ä¢")
            toc += f"{emoji} *–ì–ª–∞–≤–∞ {i}:* {chapter}\n"
        
        # –î–æ–±–∞–≤–ª—è–µ–º –µ—â–µ –æ–¥–∏–Ω —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å –ø–æ—Å–ª–µ –æ–≥–ª–∞–≤–ª–µ–Ω–∏—è
        toc += "\n‚îó‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îõ"
        
        title_message += toc
        messages.append(title_message)
        
        # –ó–∞—Ç–µ–º —Å–æ–∑–¥–∞–µ–º –æ—Ç–¥–µ–ª—å–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –¥–ª—è –∫–∞–∂–¥–æ–π –≥–ª–∞–≤—ã
        for i, chapter in enumerate(chapters, 1):
            if chapter in chapter_contents and chapter_contents[chapter]:
                # –û—á–∏—â–∞–µ–º —Ç–µ–∫—Å—Ç –≥–ª–∞–≤—ã –æ—Ç –ø–æ–≤—Ç–æ—Ä—è—é—â–∏—Ö—Å—è –∑–∞–≥–æ–ª–æ–≤–∫–æ–≤
                chapter_text = '\n'.join(chapter_contents[chapter])
                
                # –£–¥–∞–ª—è–µ–º –Ω–æ–º–µ—Ä –≥–ª–∞–≤—ã –∏–∑ —Ç–µ–∫—Å—Ç–∞, –µ—Å–ª–∏ –æ–Ω –µ—Å—Ç—å
                chapter_text = re.sub(r'^\d+[\.\:\)]?\s*', '', chapter_text)
                
                # –£–¥–∞–ª—è–µ–º –Ω–∞–∑–≤–∞–Ω–∏–µ –≥–ª–∞–≤—ã –∏–∑ —Ç–µ–∫—Å—Ç–∞, –µ—Å–ª–∏ –æ–Ω–æ –µ—Å—Ç—å
                chapter_text = re.sub(re.escape(chapter), '', chapter_text, flags=re.IGNORECASE)
                
                # –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º —Ç–µ–∫—Å—Ç —Å —É–ª—É—á—à–µ–Ω–Ω—ã–º —Ä–∞–∑–¥–µ–ª–µ–Ω–∏–µ–º –∏ –≤—ã–¥–µ–ª–µ–Ω–∏–µ–º
                emoji = chapter_emoji.get(chapter, "‚Ä¢")
                
                # –î–æ–±–∞–≤–ª—è–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ —Å –≤—ã–¥–µ–ª–µ–Ω–∏–µ–º –∏ –¥–µ–∫–æ—Ä–∞—Ç–∏–≤–Ω—ã–º —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª–µ–º
                chapter_message = f"{emoji} *–ì–õ–ê–í–ê {i}: {chapter.upper()}*\n\n"
                chapter_message += f"‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà\n\n"
                
                # –†–∞–∑–±–∏–≤–∞–µ–º —Ç–µ–∫—Å—Ç –≥–ª–∞–≤—ã –Ω–∞ –∞–±–∑–∞—Ü—ã –∏ –ø—É–Ω–∫—Ç—ã –¥–ª—è –ª—É—á—à–µ–π —á–∏—Ç–∞–µ–º–æ—Å—Ç–∏
                # –ò—Å–ø–æ–ª—å–∑—É–µ–º –±–æ–ª–µ–µ –∞–≥—Ä–µ—Å—Å–∏–≤–Ω–æ–µ —Ä–∞–∑–¥–µ–ª–µ–Ω–∏–µ —Ç–µ–∫—Å—Ç–∞
                raw_text = chapter_text.strip()
                
                # –†–∞–∑–¥–µ–ª—è–µ–º –Ω–∞ –∞–±–∑–∞—Ü—ã –ø–æ –¥–≤–æ–π–Ω—ã–º –ø–µ—Ä–µ–Ω–æ—Å–∞–º
                paragraphs = re.split(r'\n{2,}', raw_text)
                
                # –ï—Å–ª–∏ –ø–æ–ª—É—á–∏–ª—Å—è –æ–¥–∏–Ω –±–æ–ª—å—à–æ–π –∞–±–∑–∞—Ü, —Ä–∞–∑–±–∏–≤–∞–µ–º –µ–≥–æ –Ω–∞ –±–æ–ª–µ–µ –º–µ–ª–∫–∏–µ —á–∞—Å—Ç–∏ –ø–æ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è–º
                if len(paragraphs) <= 2 and any(len(p) > 400 for p in paragraphs):
                    new_paragraphs = []
                    for paragraph in paragraphs:
                        # –†–∞–∑–±–∏–≤–∞–µ–º –¥–ª–∏–Ω–Ω—ã–µ –∞–±–∑–∞—Ü—ã –Ω–∞ —Å–º—ã—Å–ª–æ–≤—ã–µ –±–ª–æ–∫–∏ –ø–æ —Ç–æ—á–∫–∞–º
                        sentences = re.split(r'(?<=[.!?])\s+', paragraph)
                        
                        # –ì—Ä—É–ø–ø–∏—Ä—É–µ–º –ø–æ 2-3 –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è –≤ –æ–¥–∏–Ω –∞–±–∑–∞—Ü
                        for i in range(0, len(sentences), 2):
                            if i+1 < len(sentences):
                                new_paragraphs.append(sentences[i] + " " + sentences[i+1])
                            else:
                                new_paragraphs.append(sentences[i])
                    paragraphs = new_paragraphs
                
                formatted_paragraphs = []
                
                # –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –∫–∞–∂–¥—ã–π –∞–±–∑–∞—Ü
                for idx, paragraph in enumerate(paragraphs):
                    # –£–±–∏—Ä–∞–µ–º –ª–∏—à–Ω–∏–µ –ø–µ—Ä–µ–Ω–æ—Å—ã —Å—Ç—Ä–æ–∫ –≤–Ω—É—Ç—Ä–∏ –∞–±–∑–∞—Ü–∞
                    clean_paragraph = re.sub(r'\n+', ' ', paragraph.strip())
                    
                    # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º –ø—É—Å—Ç—ã–µ –∞–±–∑–∞—Ü—ã
                    if not clean_paragraph:
                        continue
                    
                    # –†–∞–∑–±–∏–≤–∞–µ–º –±–æ–ª—å—à–∏–µ –∞–±–∑–∞—Ü—ã –Ω–∞ –ø—É–Ω–∫—Ç—ã, –µ—Å–ª–∏ –≤ –Ω–∏—Ö –µ—Å—Ç—å –ø–µ—Ä–µ—á–∏—Å–ª–µ–Ω–∏—è
                    if len(clean_paragraph) > 300 and ("," in clean_paragraph or ";" in clean_paragraph):
                        # –ò—â–µ–º –ø–µ—Ä–µ—á–∏—Å–ª–µ–Ω–∏—è —á–µ—Ä–µ–∑ –∑–∞–ø—è—Ç—ã–µ/—Ç–æ—á–∫–∏ —Å –∑–∞–ø—è—Ç–æ–π –∏ –¥–æ–±–∞–≤–ª—è–µ–º –º–∞—Ä–∫–µ—Ä—ã —Å–ø–∏—Å–∫–∞
                        if ":" in clean_paragraph:
                            intro, items_text = clean_paragraph.split(":", 1)
                            items = re.split(r'[,;]\s+', items_text)
                            
                            bullet_list = [intro + ":"]
                            for item in items:
                                if item.strip():
                                    bullet_list.append(f"‚Ä¢ {item.strip()}")
                            
                            clean_paragraph = "\n".join(bullet_list)
                    
                    # –î–æ–±–∞–≤–ª—è–µ–º –ø–æ–¥–∑–∞–≥–æ–ª–æ–≤–∫–∏ –¥–ª—è —É–ª—É—á—à–µ–Ω–∏—è —Å—Ç—Ä—É–∫—Ç—É—Ä—ã (–¥–ª—è –ø–µ—Ä–≤—ã—Ö –∞–±–∑–∞—Ü–µ–≤ –∫–∞–∂–¥–æ–π —Ç–µ–º—ã)
                    if idx == 0 and chapter == "–ò—Å—Ç–æ–∫–∏ –∏ –ø—Ä–µ–¥–ø–æ—Å—ã–ª–∫–∏":
                        clean_paragraph = f"*–ö–æ–Ω—Ç–µ–∫—Å—Ç —ç–ø–æ—Ö–∏:*\n{clean_paragraph}"
                    elif idx == 0 and chapter == "–ö–ª—é—á–µ–≤—ã–µ —Å–æ–±—ã—Ç–∏—è":
                        clean_paragraph = f"*–•—Ä–æ–Ω–æ–ª–æ–≥–∏—è —Å–æ–±—ã—Ç–∏–π:*\n{clean_paragraph}"
                    elif idx == 0 and chapter == "–ò—Å—Ç–æ—Ä–∏—á–µ—Å–∫–∏–µ –ª–∏—á–Ω–æ—Å—Ç–∏":
                        clean_paragraph = f"*–ö–ª—é—á–µ–≤—ã–µ —Ñ–∏–≥—É—Ä—ã:*\n{clean_paragraph}"
                    elif idx == 0 and chapter == "–ú–µ–∂–¥—É–Ω–∞—Ä–æ–¥–Ω—ã–π –∫–æ–Ω—Ç–µ–∫—Å—Ç":
                        clean_paragraph = f"*–í–Ω–µ—à–Ω–µ–ø–æ–ª–∏—Ç–∏—á–µ—Å–∫–∏–π —Ñ–æ–Ω:*\n{clean_paragraph}"
                    elif idx == 0 and chapter == "–ò—Å—Ç–æ—Ä–∏—á–µ—Å–∫–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ":
                        clean_paragraph = f"*–í–ª–∏—è–Ω–∏–µ –∏ –ø–æ—Å–ª–µ–¥—Å—Ç–≤–∏—è:*\n{clean_paragraph}"
                    
                    # –í—ã–¥–µ–ª—è–µ–º –¥–∞—Ç—ã –∏ –≤–∞–∂–Ω—ã–µ —Å–æ–±—ã—Ç–∏—è –∂–∏—Ä–Ω—ã–º —à—Ä–∏—Ñ—Ç–æ–º
                    # –ü–∞—Ç—Ç–µ—Ä–Ω –¥–ª—è –¥–∞—Ç (–Ω–∞–ø—Ä–∏–º–µ—Ä, 1812 –≥–æ–¥, 18-19 –≤–µ–∫, XVIII –≤.)
                    clean_paragraph = re.sub(r'(\d{4}(-\d{4})? (–≥–æ–¥|–≥–≥)|\d{1,2}-\d{1,2} –≤–µ–∫|[XIV]{1,5} –≤\.)', r'*\1*', clean_paragraph)
                    
                    # –í—ã–¥–µ–ª—è–µ–º –∏–º–µ–Ω–∞ –∏—Å—Ç–æ—Ä–∏—á–µ—Å–∫–∏—Ö –ª–∏—á–Ω–æ—Å—Ç–µ–π
                    clean_paragraph = re.sub(r'(—Ü–∞—Ä—å|–∏–º–ø–µ—Ä–∞—Ç–æ—Ä|–∫–Ω—è–∑—å|–∫–æ—Ä–æ–ª–µ–≤–∞|–ø—Ä–µ–º—å–µ—Ä-–º–∏–Ω–∏—Å—Ç—Ä|–ø—Ä–µ–∑–∏–¥–µ–Ω—Ç|–≤–æ–µ–Ω–∞—á–∞–ª—å–Ω–∏–∫) ([–ê-–Ø][–∞-—è]+ [–ê-–Ø][–∞-—è]+)', r'\1 *\2*', clean_paragraph)
                    
                    # –í—ã–¥–µ–ª—è–µ–º –∫–ª—é—á–µ–≤—ã–µ —Ç–µ—Ä–º–∏–Ω—ã –∏ –ø–æ–Ω—è—Ç–∏—è
                    key_terms = ["—Ä–µ—Ñ–æ—Ä–º–∞", "—Ä–µ–≤–æ–ª—é—Ü–∏—è", "–≤–æ–π–Ω–∞", "–¥–æ–≥–æ–≤–æ—Ä", "–≤–æ—Å—Å—Ç–∞–Ω–∏–µ", "–º–∞–Ω–∏—Ñ–µ—Å—Ç", "—É–∫–∞–∑"]
                    for term in key_terms:
                        clean_paragraph = re.sub(rf'({term}[–∞-—è]*)', r'_\1_', clean_paragraph, flags=re.IGNORECASE)
                    
                    formatted_paragraphs.append(clean_paragraph)
                
                # –°–æ–µ–¥–∏–Ω—è–µ–º –∞–±–∑–∞—Ü—ã —Å –≤–∏–∑—É–∞–ª—å–Ω—ã–º —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª–µ–º –º–µ–∂–¥—É –Ω–∏–º–∏ –¥–ª—è –ª—É—á—à–µ–π —á–∏—Ç–∞–µ–º–æ—Å—Ç–∏
                chapter_message += "\n\n".join(formatted_paragraphs)
                
                # –î–æ–±–∞–≤–ª—è–µ–º –Ω–æ–º–µ—Ä–∞ –≥–ª–∞–≤ –¥–ª—è —É–¥–æ–±–Ω–æ–π –Ω–∞–≤–∏–≥–∞—Ü–∏–∏ —Å –≤—ã–¥–µ–ª–µ–Ω–∏–µ–º
                if i < len(chapters):
                    chapter_message += f"\n\n‚Ä¢‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚Ä¢\n\n‚û°Ô∏è *–î–∞–ª–µ–µ:* –ì–ª–∞–≤–∞ {i+1}: {chapters[i]}"
                else:
                    chapter_message += f"\n\n‚Ä¢‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚îà‚Ä¢\n\nüìù *–ö–æ–Ω–µ—Ü –º–∞—Ç–µ—Ä–∏–∞–ª–∞*"
                
                messages.append(chapter_message)
            else:
                # –ï—Å–ª–∏ –≥–ª–∞–≤–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞, –¥–æ–±–∞–≤–ª—è–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ –±–µ–∑ —Å–æ–¥–µ—Ä–∂–∞–Ω–∏—è —Å —É–ª—É—á—à–µ–Ω–Ω—ã–º —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ–º
                emoji = chapter_emoji.get(chapter, "‚Ä¢")
